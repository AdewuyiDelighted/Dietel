package ClassAssessment;

public class Multiply {
    public static void main(String[] args) {
        int numberOne = 2;
        int numberTwo = 5;
        int number = 1;
        int count = 1;

        while (number <= numberTwo) {
            count *= numberOne;

            number++;

        }
        System.out.println(count);
    }
    }

